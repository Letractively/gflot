    public Widget createExample()
    {

        PlotModel model = new PlotModel();
        PlotOptions plotOptions = new PlotOptions();

        plotOptions.setLegendOptions( new LegendOptions().setPosition( LegendPosition.SOUTH_WEST ) );

        // add tick formatter to the options
        plotOptions.addXAxisOptions( new TimeSeriesAxisOptions() );
        plotOptions.addYAxisOptions( new AxisOptions().setMinimum( 0 ) );
        plotOptions.addYAxisOptions( new AxisOptions().setAlignTicksWithAxis( 1 ).setPosition( AxisPosition.RIGHT ).setTickDecimals( 3 )
            .setTickFormatter( new TickFormatter()
            {
                @Override
                public String formatTickValue( double tickValue, Axis axis )
                {
                    double round = Math.pow( 10, axis.getTickDecimals() );
                    return ( Math.round( tickValue * round ) / round ) + "\u20ac";
                }
            } ) );

        // create a series
        SeriesHandler oilPrices = model.addSeries( "Oil price ($)" );
        addOilPricesData( oilPrices );

        SeriesHandler exchangeRates = model.addSeries( new Series().setLabel( "USD/EUR exchange rate" ).setYAxis( 2 ) );
        addExchangeRatesDate( exchangeRates );

        // create the plot
        final SimplePlot plot = new SimplePlot( model, plotOptions );

        // put it on a panel
        FlowPanel panel = new FlowPanel();
        panel.add( plot );
        panel
            .add( new HTML(
                "<p>Multiple axis support showing the raw oil price in US $/barrel of crude oil vs. the exchange rate from US $ to â‚¬.</p><p>As illustrated, you can put in multiple axes if you need to. For each data series, simply specify the axis number. In the options, you can then configure where you want the extra axes to appear.</p>" ) );

        HorizontalPanel toolbarPanel = new HorizontalPanel();
        toolbarPanel.setSpacing( 5 );
        toolbarPanel.setVerticalAlignment( HasVerticalAlignment.ALIGN_MIDDLE );
        toolbarPanel.add( new Label( "Position axis" ) );
        Button left = new Button( "left" );
        toolbarPanel.add( left );
        toolbarPanel.add( new Label( "or" ) );
        Button right = new Button( "right" );
        toolbarPanel.add( right );

        left.addClickHandler( new ClickHandler()
        {
            @Override
            public void onClick( ClickEvent event )
            {
                plot.getPlotOptions().getYAxisOptions( 2 ).setPosition( AxisPosition.LEFT );
                plot.redraw();
            }
        } );

        right.addClickHandler( new ClickHandler()
        {
            @Override
            public void onClick( ClickEvent event )
            {
                plot.getPlotOptions().getYAxisOptions( 2 ).setPosition( AxisPosition.RIGHT );
                plot.redraw();
            }
        } );

        panel.add( toolbarPanel );

        return panel;
    }

